function chalkReplacer(chalk: number[], k: number): number {
  let sum: number = 0;
  for (let i = 0; i < chalk.length; i++) {
    sum += chalk[i];
  }
  console.log('Sum', sum);
  let remaining = k % sum;
  console.log('Remaining', remaining);
  if (remaining === 0) {
    return 0;
  } else {
    for (let i = 0; i < chalk.length; i++) {
      if (remaining - chalk[i] < 0) {
        return i;
      } else {
        remaining -= chalk[i];
      }
    }
  }
  return 0;
}

